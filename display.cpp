static Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, OLED_RESET);
static Preferences      dispPrefs;

// Custom bitmap buffer (1KB di RAM)
static uint8_t customBitmapBuf[1024];
static bool    customBitmapLoaded = false;

DisplayConfig displayConfig = {
  255,   // brightness
  255,   // contrast
  0,     // displayMode
  5000,  // displayTimeout
  2,     // fontSize
  500,   // updateRate
  false  // customLogo
};

// ============================================================
//  SETUP
//  Default boot screen: tampilkan IP WebServer.
//  Jika ada custom logo → logo di area atas (48px),
//  IP di area bawah (16px).
// ============================================================
bool displaySetup(const String& ipAddress) {
  loadCustomBitmap();

  if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("[DISPLAY] SSD1306 init failed"));
    return false;
  }

  showIP(ipAddress);
  return true;
}

// ============================================================
//  SHOW IP — layar default saat boot
//
//  Layout (128×64):
//  ┌──────────────────────────────┐
//  │  [logo 48px tinggi jika ada] │
//  │  atau kosong jika tidak ada  │
//  ├──────────────────────────────┤  y=48
//  │  WiFi: WIRAGORA_2024         │  ukuran 1
//  │  192.168.4.1                 │  ukuran 1
//  └──────────────────────────────┘
// ============================================================
void showIP(const String& ipAddress) {
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);

  if (customBitmapLoaded) {
    // Logo custom di area atas (48px dari 64px total)
    // Bitmap di-crop vertikal — hanya 48 baris pertama ditampilkan
    display.drawBitmap(0, 0, customBitmapBuf, 128, 48, SSD1306_WHITE);
  }

  // Garis pemisah
  display.drawFastHLine(0, 49, 128, SSD1306_WHITE);

  // Teks IP di bawah
  display.setTextSize(1);
  display.setCursor(0, 52);
  display.print(F("WiFi: WIRAGORA_2024"));
  display.setCursor(20, 56); // sedikit indent agar terlihat rapi
  // IP di baris terakhir dengan sedikit lebih besar
  display.setTextSize(1);
  display.setCursor(0, 56);
  display.print(ipAddress);

  display.display();
}

// ============================================================
//  SHOW LOGO — static, tidak ada scroll
//  Dipanggil dari tombol SELECT PS3
// ============================================================
void showLogo() {
  display.clearDisplay();
  if (customBitmapLoaded) {
    display.drawBitmap(0, 0, customBitmapBuf, 128, 64, SSD1306_WHITE);
  } else {
    display.drawBitmap(0, 0, epd_bitmap_Logo_Polindra, 128, 64, SSD1306_WHITE);
  }
  display.display();
}

// ============================================================
//  SHOW BATT STATUS
// ============================================================
static void drawBatteryIndicator(int level) {
  display.drawRect(90, 0, 30, 15, SSD1306_WHITE);
  display.fillRect(120, 4, 3, 7, SSD1306_WHITE);
  int width = map(level, 0, 100, 0, 28);
  display.fillRect(91, 1, width, 13, SSD1306_WHITE);
}

void showBattStatus(int battLevel, const String& battMessage) {
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  display.setTextSize(2);
  display.setCursor(0, 0);
  display.println(F("Battery PS3:"));
  display.setTextSize(1);
  display.setCursor(30, 30);
  display.println(battMessage);
  drawBatteryIndicator(battLevel);
  display.display();
}

// ============================================================
//  SHOW E-STOP
// ============================================================
void showEStop(bool active) {
  display.clearDisplay();
  if (active) {
    display.drawRect(0, 0, 128, 64, SSD1306_WHITE);
    display.drawRect(2, 2, 124, 60, SSD1306_WHITE);
    display.setTextColor(SSD1306_WHITE);
    display.setTextSize(3);
    display.setCursor(10, 8);
    display.println(F("E-STOP"));
    display.setTextSize(1);
    display.setCursor(8, 48);
    display.println(F("Gerak stick = resume"));
  }
  display.display();
}

